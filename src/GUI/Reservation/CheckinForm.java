    public boolean checkRoomStatus(int roomNumber) {
        DataConnection dc = new DataConnection();
        Connection conn = dc.openConnection();
        String query = "select * from Room where RoomNumber = " + roomNumber;
        boolean status = false;
        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(query);
            while (rs.next()) {
                int roomStatusID = rs.getInt("RoomStatusID");
                if (roomStatusID == 8) {
                    status = true;
                }
            }
            conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(CheckinForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return status;
    }

