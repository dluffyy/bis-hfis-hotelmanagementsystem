    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int roomNumber = Integer.parseInt(txtRoomNumber.getText());
        boolean status = this.checkRoomStatus(roomNumber);
        if (status == true) {
            DataConnection dc = new DataConnection();
            Connection conn = dc.openConnection();
            String reservationID = txtReservationID.getText();
            String checkindate = txtCheckinDate.getText();
            String query = "Exec new_Checkin " + reservationID + "," + roomNumber + ",'" + checkindate + "'";
            String query2 = "Exec update_Room " + roomNumber + "," + 1;
            try {
                PreparedStatement ps = conn.prepareCall(query);
                ps.executeUpdate();
                PreparedStatement ps1 = conn.prepareCall(query2);
                ps1.executeUpdate();
                JOptionPane.showMessageDialog(this, "Success !");
                conn.close();
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CheckinForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (status == false) {
            JOptionPane.showMessageDialog(this, "This room is not available to be used !");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

